    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession(false);
            if (session == null || session.getAttribute("user") == null) {
                response.sendRedirect("home");
                return;
            }
        
        User user = (User) request.getSession().getAttribute("user");
        request.setAttribute("user", new UserDAO().getUserByUsername(user.getUsername()));
        
        request.getRequestDispatcher("profile.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        
        String username = request.getParameter("username");
        String fullName = request.getParameter("fullName");
        String email = request.getParameter("email");
        String dob = request.getParameter("dob");
        int gender = Integer.parseInt(request.getParameter("gender"));

        // Create a User object with the updated profile information
        User updatedUser = new User(username, null, fullName, email, dob, gender, 0, 0);

        // Update the user's profile in the database
        UserDAO userDAO = new UserDAO();
        userDAO.updateUserProfile(updatedUser);
        
        response.sendRedirect("profile");
    }

